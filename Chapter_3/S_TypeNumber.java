package chapter_3;

public class S_TypeNumber {
    public static void main(String[] args){
        // 'main+Enter' 누르면 메인 메소드가 (윗줄과 같이) 자동 생성된다.

        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a+b+15);
        //'sout' 라고 치면 명령문이 (윗줄과 같이) 자동 완성된다.
    }
}
